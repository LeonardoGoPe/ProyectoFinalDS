    private void EntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EntrarActionPerformed
        evt.getID();
        try {
            switch(cnx.validarUsuario(txtUser.getText(), password.getText())){
                case "Vendedor":
                    VistaVendedor v = new VistaVendedor();
                    v.setVisible(true);
                    break;
                    
                case "Gerente":
                    VistaGerente g = new VistaGerente();
                    g.setVisible(true);
                    break;
                    
                case "Jefe de bodega":
                    VistaJefeBodega jb = new VistaJefeBodega();
                    jb.setVisible(true);
                    break;
                    
                case "Administrador":
                    VistaAdministrador a = new VistaAdministrador();
                    a.setVisible(true);
                    break;
                    
                default:
                    //No registrado
                    break;
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginVista.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_EntrarActionPerformed

